
function mesh = mkcmesh_NACA65_X10(X,porder,meshparams,elemtype,bndexpr,a,plotFlag,transitionLocation)

if nargin<7; plotFlag=1; end         % Flag for plotting the mesh.
if nargin<4, elemtype=0; end
if nargin<8; transitionLocation = [0.5,0.5]; end
if nargin<6; a=1.0; end

n1  = meshparams.n1;
n2  = meshparams.n2;
n2r = meshparams.n2r;
n3  = meshparams.n3;
sps = meshparams.sps;
spr = meshparams.spr;

[x,y] = cmeshTransition(n1, n2r, n2, n2, n2r, n3, sps, spr, transitionLocation);

minThickness = 0;
[xf,yf] = NACA65_X10(X,a,minThickness);       % Airfoil extends from xf=0 to xf=1.

[xm, ym] = cmeshmap(xf, yf, x, y, 1, 1);

% Fix the wake gap:
xm(1,1:n1) = xm(1,end:-1:end-(n1-1));       % -> n1 may need to be replaced here in the future.
ym(1,1:n1) = ym(1,end:-1:end-(n1-1));       % -> n1 may need to be replaced here in the future.

mesh = cart2mesh(porder,xm,ym,[],bndexpr,elemtype);

if plotFlag; meshplot(mesh,1); xlim([-0.5 1.125]); ylim([-1 1]); end

end



   
